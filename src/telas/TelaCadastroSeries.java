    public void listar(){
        try {
            // executando o select
        SerieDAO dao = new SerieDAO();
        List<Serie> listSerie = dao.listaSeries();
        
        // colocando os dados na tabela
        DefaultTableModel model = (DefaultTableModel) tabelaSeries.getModel();
        model.setRowCount(0);
        
        for(Serie s : listSerie){
            model.addRow(new Object[]{
               s.getIdSerie(),
               s.getNome(),
               s.getGenero(),
               s.getDirecao(),
               s.getAnoLancamento(),
               s.getTemporadas(),
               s.getStreamer(),
               s.getAssistiu()
            });
        }
            
        
        } catch (Exception e) {
        }
    }
    
