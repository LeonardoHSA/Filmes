    private void botaoCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoCadastrarActionPerformed
        
        try {
            Serie obj = new Serie();
            obj.setNome(nomeTextField.getText());
            obj.setGenero(generoTextField.getText());
            obj.setDirecao(direcaoTextField.getText());
            obj.setAnoLancamento(Integer.parseInt(anoLancamentoTextField.getText()));
            obj.setTemporadas(temporadaTextField.getText());
            obj.setStreamer(streamerTextField.getText());
            
            if(assistiuCheck.isSelected()){
                obj.setAssistiu("Sim");
            } else {
                obj.setAssistiu("Não");
            }
            
            SerieDAO dao = new SerieDAO();
            dao.cadastraSerie(obj);
            
            JOptionPane.showMessageDialog(null, "Cadastro realizado com sucesso!!!");
            
        } catch (Exception erroSql) {
            JOptionPane.showMessageDialog(null, "Erro ao cadastrar: " + erroSql);
        }
    }//GEN-LAST:event_botaoCadastrarActionPerformed

